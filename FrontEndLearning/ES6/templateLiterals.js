// ES5 string concatenation
var name = 'Panda';
var age = 99;
var message = 'Hello, my name is ' + name + ' and my age is ' + age;
console.log('ES5 concatenation', message);

// ES6 template literals
const firstName = 'Bruce';
const lastName = 'Lee';
const fullName = `Hello, my name is ${firstName} ${lastName}`;
console.log('ES6 template literals', fullName);
